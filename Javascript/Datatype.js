// typeof = เช็คชนิดข้อมูล
// null = ไม่มีการกำหนดค่า(ถูกกำหนดค่าโดยผู้เขียน)
// undefined = ไม่มีการกำหนดค่า(ถูกกำหนดค่าโดยโปรแกรม)

// การจัดการNumber
let x,y;
x=20+100; // integer
console.log(typeof(x));
console.log(x);
y=20.15; // float
console.log(typeof(y));
console.log(y);

// การจัดการString
let a = 'Pd';
console.log(typeof(a));
console.log(a);
let b = "20+10"; // = 20+10
console.log(typeof(b));
console.log(b);
let c = "20"+10; // = 2010
console.log(typeof(c));
console.log(c);

// การจัดการ boolean
let check1 = true;
console.log(typeof(check1));
console.log(check1);
let check2 = false;
console.log(typeof(check2));
console.log(check2);

